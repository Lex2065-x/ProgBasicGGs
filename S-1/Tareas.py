print("Conceptos de Computadora y sus Equivalentes Humanos:")
print("1. Unidad Central de Procesamiento (CPU):")
print("   - Computadora: La CPU es el cerebro de la computadora. Se encarga de ejecutar las instrucciones y tomar decisiones.")
print("   - Humano: El cerebro humano realiza procesos similares, como resolver problemas y tomar decisiones basadas en información.")
print("2. Memoria Principal (RAM):")
print("   - Computadora: La memoria RAM almacena temporalmente los datos e instrucciones que la computadora necesita mientras está funcionando.")
print("   - Humano: La memoria a corto plazo de un ser humano es similar, como recordar temporalmente un número de teléfono mientras lo marcas.")
print("3. Memoria Secundaria:")
print("   - Computadora: La memoria secundaria (disco duro, SSD) guarda información de manera permanente, incluso después de apagar la computadora.")
print("   - Humano: La memoria a largo plazo es equivalente a recordar eventos importantes de la infancia o almacenar información en un diario.")
print("4. Dispositivos de Entrada:")
print("   - Computadora: Los dispositivos de entrada como el teclado o el ratón permiten que la computadora reciba información del exterior.")
print("   - Humano: Los sentidos (vista, oído, tacto) permiten que los humanos reciban información del mundo exterior, como leer o escuchar.")
print("5. Dispositivos de Salida:")
print("   - Computadora: Los dispositivos de salida como el monitor o la impresora permiten que la computadora muestre resultados al usuario.")
print("   - Humano: Las respuestas físicas de los humanos, como hablar o escribir, son análogas a los dispositivos de salida de una computadora.")

